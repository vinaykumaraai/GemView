package com.luretechnologies.mailslurper;

import java.util.HashMap;
import java.util.Map;
import com.fasterxml.jackson.annotation.JsonAnyGetter;
import com.fasterxml.jackson.annotation.JsonAnySetter;
import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonPropertyOrder;

@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonPropertyOrder({
    "mailCount"
})
public class MailCount {

    @JsonProperty("mailCount")
    private Integer mailCount;
    @JsonIgnore
    private Map<String, Object> additionalProperties;

    /**
     * No args constructor for use in serialization
     *
     */
    public MailCount() {
        this.additionalProperties = new HashMap<>();
    }

    /**
     *
     * @param mailCount
     */
    public MailCount(Integer mailCount) {
        super();
        this.additionalProperties = new HashMap<>();
        this.mailCount = mailCount;
    }

    @JsonProperty("mailCount")
    public Integer getMailCount() {
        return mailCount;
    }

    @JsonProperty("mailCount")
    public void setMailCount(Integer mailCount) {
        this.mailCount = mailCount;
    }

    public MailCount withMailCount(Integer mailCount) {
        this.mailCount = mailCount;
        return this;
    }

    @JsonAnyGetter
    public Map<String, Object> getAdditionalProperties() {
        return this.additionalProperties;
    }

    @JsonAnySetter
    public void setAdditionalProperty(String name, Object value) {
        this.additionalProperties.put(name, value);
    }

    public MailCount withAdditionalProperty(String name, Object value) {
        this.additionalProperties.put(name, value);
        return this;
    }
}
